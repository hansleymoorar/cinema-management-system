            String sql ="UPDATE MOVIE SET MOVIENAME=?, GENRE=?, AGE=?, RATING=?,DURATION=?, REVIEW=?, MOVIECAST=?, TICKETSAVAILABLE=? WHERE MOVIEID=?";
            ps=con.prepareStatement(sql);
